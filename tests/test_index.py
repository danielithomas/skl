"""Tests for ``skl.index`` and the ``skl index`` CLI verb."""

from __future__ import annotations

from pathlib import Path
from textwrap import dedent

import pytest
from click.testing import CliRunner

from skl import __version__
from skl.cli import main
from skl.index import regenerate_index


def _make_repo(tmp_path: Path) -> Path:
    repo = tmp_path / "repo"
    repo.mkdir()
    (repo / "skill-repo.yaml").write_text(
        f"""\
schema_version: 1
name: host
visibility: internal
skl_version: ">={__version__},<99.0"
shared_kit:
  source: github.com/example/kit
  version: "1.0.0"
enabled_platforms: []
"""
    )
    return repo


def _make_skill(repo: Path, name: str, *, content: str | None = None) -> Path:
    skill_root = repo / "skills" / name
    skill_root.mkdir(parents=True)
    if content is None:
        content = dedent(
            f"""\
            ---
            name: {name}
            description: A skill named {name}.
            skl:
              schema_version: 1
              display_name: {name.title()}
              status: active
              lifecycle: business_development
              enabled_platforms: [claude-code, m365]
            ---

            # {name.title()}

            ## Capabilities
            - Things.

            ## Workflow
            1. Do.

            ## Edge Cases
            - None.

            ## Examples
            Use.
            """
        )
    (skill_root / "SKILL.md").write_text(content)
    return skill_root


