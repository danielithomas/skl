@pytest.fixture
def runner() -> CliRunner:
    return CliRunner()


def test_cli_index_outside_repo_errors(runner: CliRunner, tmp_path: Path) -> None:
    with runner.isolated_filesystem(temp_dir=tmp_path):
        result = runner.invoke(main, ["index"])
        assert result.exit_code != 0
        assert "not inside a skill-host repo" in result.output


def test_cli_index_writes_file(
    runner: CliRunner, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    repo = _make_repo(tmp_path)
    _make_skill(repo, "alpha")
    monkeypatch.chdir(repo)
    result = runner.invoke(main, ["index"])
    assert result.exit_code == 0, result.output
    assert "wrote skills/SKILLS_INDEX.md" in result.output
    assert (repo / "skills" / "SKILLS_INDEX.md").is_file()


def test_cli_index_no_skills_dir_errors(
    runner: CliRunner, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    repo = _make_repo(tmp_path)
    monkeypatch.chdir(repo)
    result = runner.invoke(main, ["index"])
    assert result.exit_code != 0
    assert "no skills/ directory" in result.output
